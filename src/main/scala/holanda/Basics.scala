package holanda

import scala.io.StdIn._

private class Basics() {
  
  def sum(a: Double, b: Double): Double =
    val soma = a + b
    return soma
    
  def sub(a: Double, b: Double): Double =
    val subt = a - b
    return subt
    
  def mult(a: Double, b: Double): Double =
    val mul = a * b
    return mul
    
  def div(a: Double, b: Double): Double =
    val divs = a / b
    return divs
    
  def mod(a: Double, b: Double): Double =
    val modul = a % b
    return modul
  
}
